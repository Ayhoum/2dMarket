                          NAW-gegevens: Persoonlijke gegevens van een Klant waarmee deze is te identificeren, mogelijk bestaand uit, maar niet beperkt tot, naam, adres, woonplaats, telefoonnummer, mailadres en IP-nummer.<br>

                          PIP:	"Persoonlijke Internet Pagina"; een beveiligde pagina beschikbaar voor bepaalde Klanten waarop onder meer naast persoonlijke gegevens van de Klant ook wordt vermeld van welke Diensten de Klant gebruik van maakt.<br>

