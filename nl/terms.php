                          2D Market groep:    De groep ex. artikel 2:24b BW waar 2Dmarket  toe behoort.<br>

                          Diensten: Het door 2Dmarket  tot stand brengen, beschikbaar stellen, doen laten gebruiken en instandhouden, al dan niet tegen betaling, van een of meerdere diensten op het gebied van elektronische communicatie, waaronder begrepen maar niet beperkt tot het met betrekking tot internet verlenen van acces- en hostingdiensten, het aanbieden van (diensten op) het Portal en de verkoop van producten op genoemd Portal.<br>

