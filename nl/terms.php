                          3.5	Indien een Klant reeds een separate overeenkomst met 2Dmarket is aangegaan betreffende specifieke delen van de Diensten, heeft bij strijdigheid van het bepaalde in genoemde overeenkomst en deze Gebruiksvoorwaarden het bepaalde in de separate overeenkomst voorrang.<br>

